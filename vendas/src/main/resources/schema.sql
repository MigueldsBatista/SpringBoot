CREATE TABLE CLIENTE(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    NOME VARCHAR(100)
);

CREATE TABLE PRODUTO(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    DESCRICAO VARCHAR(100),
    PRECO_UNITARIO NUMERIC(20, 2)
);
CREATE TABLE PEDIDO (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    CLIENTE_ID INT,
    DATA_PEDIDO TIMESTAMP,
    TOTAL NUMERIC(20, 2),
    FOREIGN KEY(CLIENTE_ID) REFERENCES CLIENTE(ID) -- one 2 many de cliente pra pedido
);

CREATE TABLE ITEM_PEDIDO(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    PEDIDO_ID INT,
    PRODUTO_ID INT,
    QUANTIDADE INT,
    FOREIGN KEY (PEDIDO_ID) REFERENCES PEDIDO(ID), --onde 2 pedido para item pedido
    FOREIGN KEY (PRODUTO_ID) REFERENCES PRODUTO(ID)
);